select A.FromPaperID, B.AUTHOR_ID as NEW_AUTHOR_ID, A.ToPaperId, C.AUTHOR_ID as OLD_AUTHOR_ID 
FROM TESTDB.PaperCitation AS A 
INNER JOIN TESTDB.PAPER_AUTHOR AS B 
ON A.FromPaperID=CONVERT(B.PAPER_ID, UNSIGNED INTEGER) 
INNER JOIN TESTDB.PAPER_AUTHOR AS C 
ON A.ToPaperID=CONVERT(C.PAPER_ID, UNSIGNED INTEGER) INTO OUTFILE '/tmp/AUTHOR_CITE.txt'
