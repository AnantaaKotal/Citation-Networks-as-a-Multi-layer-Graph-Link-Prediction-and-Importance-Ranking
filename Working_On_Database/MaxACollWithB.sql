INSERT INTO TESTDB.MaxACollWithB


select A.FromPaperID, A.ToPaperID, max(B.CNT)
FROM TESTDB.AUTHOR_CITE AS A
INNER JOIN TESTDB.AUTH_COLL_CNT AS B
ON (A.NEW_AUTHOR_ID=B.AUTHOR_A_ID OR A.NEW_AUTHOR_ID=B.AUTHOR_B_ID)
AND (A.OLD_AUTHOR_ID=B.AUTHOR_B_ID OR A.OLD_AUTHOR_ID=B.AUTHOR_A_ID)
GROUP BY A.FromPaperID, A.ToPaperID
